    std::vector<Vector2d> zip(const double x[], const double y[], const size_t count)
    {
        std::vector<Vector2d> points(count);

        for (size_t i = 0; i < count; ++i)
        {
            points[i].x = x[i];
            points[i].y = y[i];
        }

        return points;
    }

